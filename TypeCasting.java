public class TypeCasting {
    public static void main(String[] args) {
        float a = 10 / 3;
        System.out.println(a);
        //10 is int and 3 is int
        a = (float) 10/ 3;
        System.out.println(a);

        int b = 10;
        System.out.println(b);
        System.out.println(b++);
        System.out.println(b);
        System.out.println(++b);


    }
}
